def main():
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    updated = 0
    skipped = 0

    for seed_id, data in SEED_DATA.items():
        # Build SET clause only for non-None fields
        fields = []
        values = []
        for key, val in data.items():
            if val is not None:
                fields.append(f"{key} = ?")
                values.append(val)

        if not fields:
            skipped += 1
            continue

        values.append(seed_id)
        sql = f"UPDATE seeds SET {', '.join(fields)} WHERE id = ?"

        try:
            cursor.execute(sql, values)
            if cursor.rowcount > 0:
                updated += 1
                print(f"  Updated #{seed_id}: {data.get('latin_name', 'unknown')}")
            else:
                print(f"  SKIP #{seed_id}: not found in database")
                skipped += 1
        except Exception as e:
            print(f"  ERROR #{seed_id}: {e}")
            skipped += 1

    conn.commit()
    conn.close()
    print(f"\nDone: {updated} updated, {skipped} skipped")


if __name__ == "__main__":
    main()
